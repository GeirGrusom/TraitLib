namespace Traitor
{
    /// <summary>
    /// Defines result types from a novel trait
    /// </summary>
    public enum NovelResultType
    {
        /// <summary>
        /// No novel trait is added or removed
        /// </summary>
        None,

        /// <summary>
        /// A novel trait is added
        /// </summary>
        Add,

        /// <summary>
        /// A novel trait is removed
        /// </summary>
        Remove
    }
}
